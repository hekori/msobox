C ===============================================================================
C
C fortran ffcn for the chemical reaction example
C
C 	x1_dot = - u * x1 + u ** 2 * x2
C 	x2_dot = u * x1 - p * u ** 2 * x2
C   
C 	0 < u < 1 
C 
C ===============================================================================

	subroutine ffcn(t, x, f, p, u)

		implicit none

		double precision x(2), f(2), p(1), u(1), t

		f(1) = - u(1) * x(1) + u(1) ** 2 * x(2)
		f(2) = u(1) * x(1) - p(1) * u(1) ** 2 * x(2)

	end


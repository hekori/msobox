C-------------------------------------------------------------------------------
C       Model function definitions of the rocket car example.
C-------------------------------------------------------------------------------

      subroutine rfcn_0(r, t, x)
C       ------------------------------------------------------------------------
        ! Start point constraint ensuring the reaching of a fixed end position and
        ! velocity, given by:

        !       r(0, x) = [x[0] - 0] = 0
        !                 [x[1] - 0]
C       ------------------------------------------------------------------------
        implicit none
        double precision r(2), t(1), x(2)
C       ------------------------------------------------------------------------
        r(1) = x(1) - 0.0d0
        r(2) = x(2) - 0.0d0
C       ------------------------------------------------------------------------
      end


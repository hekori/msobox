C ===============================================================================
C
C fortran ffcn for the rocketcar example
C
C 	x1_dot = x2
C 	x2_dot = u
C
C 	-1 < u < 1
C
C ===============================================================================

	subroutine ffcn(f, t, x, p, u)

		implicit none

		double precision f(3), t, x(3), p(1), u(1)

		f(1) = x(2) * x(3)
		f(2) = u(1) * x(3)
		f(3) = 0

	end


	subroutine ffcn(f, t, x, p, u)

		implicit none

		double precision f(2), t, x(2), p(1), u(1)

		f(1) = -u(1) * x(1) + u(1) ** 2 * x(2)
		f(2) = u(1) * x(1) - p(1) * u(1) ** 2 * x(2)

	end

